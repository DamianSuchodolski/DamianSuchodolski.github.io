async function handleSubmit(e) {
  e.preventDefault();
  const form = e.target;
  const msg = document.getElementById('form-msg');
  const btn = form.querySelector('button');
  btn.textContent = t('form_sending');
  btn.disabled = true;

  const data = {
    name: form.querySelector('[name=name]').value,
    email: form.querySelector('[name=email]').value,
    project_type: form.querySelector('[name=type]').value,
    message: form.querySelector('[name=message]').value,
    _subject: 'DS Automation — zapytanie ze strony: ' + form.querySelector('[name=name]').value,
    _template: 'table'
  };

  try {
    const res = await fetch('https://formsubmit.co/ajax/damiansuchodolski88@gmail.com', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', 'Accept': 'application/json' },
      body: JSON.stringify(data)
    });
    if (!res.ok) throw new Error('HTTP ' + res.status);
    msg.textContent = t('form_sent');
    btn.textContent = t('form_sent_btn');
    btn.style.background = '#005533';
  } catch (err) {
    msg.textContent = t('form_error');
    btn.textContent = t('form_submit');
    btn.disabled = false;
  }
}

function applyLang(lang) {
  if (!I18N.pl._captured) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      I18N.pl[el.dataset.i18n] = el.innerHTML;
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      I18N.pl['ph_' + el.dataset.i18nPh] = el.placeholder;
    });
    I18N.pl._captured = true;
  }
  const d = I18N[lang] || I18N.pl;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = d[el.dataset.i18n] !== undefined ? d[el.dataset.i18n] : I18N.pl[el.dataset.i18n];
    if (v !== undefined) el.innerHTML = v;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const k = 'ph_' + el.dataset.i18nPh;
    const v = d[k] !== undefined ? d[k] : I18N.pl[k];
    if (v !== undefined) el.placeholder = v;
  });
  if (typeof loaderMsgs !== 'undefined') {
    const msgs = d.loader_msgs || I18N.pl.loader_msgs;
    loaderMsgs.length = 0;
    loaderMsgs.push(...msgs);
  }
  window.LANG = lang;
  document.documentElement.lang = lang;
  document.title = d.page_title || I18N.pl.page_title;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.content = d.meta_desc || I18N.pl.meta_desc;
  try { localStorage.setItem('lang', lang); } catch (e) {}
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
  const pj = document.getElementById('projlog');
  if (pj && pj.classList.contains('open')) {
    document.getElementById('projlog-toggle').textContent = t('projlog_hide');
  }
}

(function() {
  let saved = 'pl';
  try { saved = localStorage.getItem('lang') || 'pl'; } catch (e) {}
  applyLang(saved);
})();
